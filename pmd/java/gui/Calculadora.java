package gui;

public class Calculadora {
	
	public static double soma(double a, double b) {
		return a + b;
	}
	
	public static double subt(double a, double b) {
		return a - b;
	}
	
	public static double mult(double a, double b) {
		return a * b;
	}
	
	public static double divi(double a, double b) {
		return a / b;
	}

}
